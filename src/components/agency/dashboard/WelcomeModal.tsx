const PLAN_DISPLAY_NAMES: Record<string, string> = {
  free: "Free",
  starter: "Starter",
  professional: "Professional",
  business: "Business",
};

export function WelcomeModal() {
  const searchParams = useSearchParams();
  const router = useRouter();
  const { planInfo, loading: planLoading } = usePlanLimits();
  const [useCase, setUseCase] = useState<UseCase>("other");
  const [isOpen, setIsOpen] = useState(false);
  const [loading, setLoading] = useState(true);

  const isSubscribed = searchParams.get("subscribed") === "true";

  useEffect(() => {
    if (!isSubscribed) return;

    async function fetchTenantInfo() {
      try {
        const res = await fetch("/api/tenant/info");
        if (res.ok) {
          const data = await res.json();
          const uc = data.use_case as UseCase;
          setUseCase(USE_CASE_ACTIONS[uc] ? uc : "other");
        }
      } catch {
        // Fall back to "other"
      } finally {
        setLoading(false);
        setIsOpen(true);
      }
    }

    fetchTenantInfo();
  }, [isSubscribed]);

  const handleDismiss = () => {
    setIsOpen(false);
    router.replace("/dashboard", { scroll: false });
  };

  if (!isSubscribed) return null;

  const actions = USE_CASE_ACTIONS[useCase];
  const planName = planInfo
    ? PLAN_DISPLAY_NAMES[planInfo.planType] || planInfo.planType
    : "";

  return (
    <Modal isOpen={isOpen} onClose={handleDismiss} className="max-w-lg p-6 sm:p-8">
      {loading || planLoading ? (
        <div className="flex items-center justify-center py-12">
          <div className="h-8 w-8 animate-spin rounded-full border-4 border-brand-500 border-t-transparent" />
        </div>
      ) : (
        <div className="text-center">
          {/* Celebration icon */}
          <div className="mx-auto mb-4 flex h-16 w-16 items-center justify-center rounded-full bg-brand-50 dark:bg-brand-500/10">
            <svg className="h-8 w-8 text-brand-500" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M9.813 15.904L9 18.75l-.813-2.846a4.5 4.5 0 00-3.09-3.09L2.25 12l2.846-.813a4.5 4.5 0 003.09-3.09L9 5.25l.813 2.846a4.5 4.5 0 003.09 3.09L15.75 12l-2.846.813a4.5 4.5 0 00-3.09 3.09zM18.259 8.715L18 9.75l-.259-1.035a3.375 3.375 0 00-2.455-2.456L14.25 6l1.036-.259a3.375 3.375 0 002.455-2.456L18 2.25l.259 1.035a3.375 3.375 0 002.455 2.456L21.75 6l-1.036.259a3.375 3.375 0 00-2.455 2.456zM16.894 20.567L16.5 21.75l-.394-1.183a2.25 2.25 0 00-1.423-1.423L13.5 18.75l1.183-.394a2.25 2.25 0 001.423-1.423l.394-1.183.394 1.183a2.25 2.25 0 001.423 1.423l1.183.394-1.183.394a2.25 2.25 0 00-1.423 1.423z" />
            </svg>
          </div>

          <h2 className="mb-2 text-xl font-semibold text-gray-900 dark:text-white">
            Welcome to your {planName} plan!
          </h2>
          <p className="mb-6 text-sm text-gray-500 dark:text-gray-400">
            Your workspace is ready. Here are some things to get started:
          </p>

          {/* Suggested actions */}
          <div className="mb-6 space-y-3">
            {actions.map((action) => (
              <a
                key={action.href}
                href={action.href}
                onClick={() => setIsOpen(false)}
                className="flex items-center gap-3 rounded-xl border border-gray-200 p-3 text-left transition-colors hover:border-brand-300 hover:bg-brand-50 dark:border-gray-700 dark:hover:border-brand-500/40 dark:hover:bg-brand-500/5"
              >
                <span className="flex h-10 w-10 shrink-0 items-center justify-center rounded-lg bg-gray-100 text-gray-600 dark:bg-gray-800 dark:text-gray-300">
                  {action.icon}
                </span>
                <span className="text-sm font-medium text-gray-700 dark:text-gray-200">
                  {action.label}
                </span>
                <svg className="ml-auto h-4 w-4 text-gray-400" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                  <path strokeLinecap="round" strokeLinejoin="round" d="M8.25 4.5l7.5 7.5-7.5 7.5" />
                </svg>
              </a>
            ))}
          </div>

          <button
            onClick={handleDismiss}
            className="w-full rounded-lg bg-brand-500 px-4 py-2.5 text-sm font-medium text-white transition-colors hover:bg-brand-600"
          >
            Go to Dashboard
          </button>
        </div>
      )}
    </Modal>
  );
}
